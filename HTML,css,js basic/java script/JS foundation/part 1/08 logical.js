// && = And
// || = Or
// ! = Not
// Logical operators are used to determine the logic between variables or values.
// Given the following variables:
let a = true;
let b = false;  
let c = true;
// What will the following statement return?
console.log(a && b); //it will return false because both the values are not true 
console.log(a || b); //it will return true because one of the value is true
console.log(!a); //it will return false because the value of a is true and ! is used to reverse the value